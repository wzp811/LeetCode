package LeetCodeContest.Weekly310;

import java.util.Arrays;
import java.util.Comparator;

public class MinGroups {
    public int minGroups(int[][] intervals) {
        Arrays.sort(intervals, new Comparator<int[]>() {
            @Override
            public int compare(int[] o1, int[] o2) {
                if (o1[0] == o2[0]) return o1[1] - o2[1];
                return o1[0] - o2[0];
            }
        });
//        for (int i = 0; i < intervals.length; i++) System.out.println(Arrays.toString(intervals[i]));
        int res = 0;
        boolean[] visited = new boolean[intervals.length];
        for (int i = 0; i < intervals.length; i++) {
            if (visited[i]) continue;
            visited[i] = true;
            int last = i;
            int index = i + 1;
            while (index < intervals.length) {
                if (intervals[index][0] > intervals[last][1]) {
                    last = index;
                    visited[index] = true;
                }
                index++;
            }
            res++;
            System.out.println(Arrays.toString(visited));
        }
        return res;
    }
}
