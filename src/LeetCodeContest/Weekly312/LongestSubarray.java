package LeetCodeContest.Weekly312;

public class LongestSubarray {
    public int longestSubarray(int[] nums) {
        int res = 1;
        int curLen = 0;
        int max = 0;
        for (int i = 0; i < nums.length; i++) {
            if (nums[i] > max){
                max = nums[i];
                curLen = 1;
                res = 1;
            } else if (nums[i] == max) {
                curLen++;
            } else if (nums[i] != max) {
                res = Math.max(curLen, res);
                curLen = 0;
            }
        }
        res = Math.max(res, curLen);
        return res;
    }
}


